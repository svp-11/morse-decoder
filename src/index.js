function decode(expr) {
    let arr = expr.split('');
    let bit = [];
    let word='';
    for(let i=0; i < arr.length; i=i+10){
        let letter = arr.slice(i, i+10).join('').replace(/11/g, "-").replace(/10/g, ".").replace(/0/g, "").replace("**********", ""); 
        word += letter ? MORSE_TABLE[letter] : " ";
    }
    return word;
}

